package org.jlab.coda.cMsg.coda;

import org.jlab.coda.cMsg.cMsgCallback;

/**
 * Class to store all information necessary to run a callback.
 */
class cMsgCallbackInfo {
    Object arg;
    cMsgCallback callback;

    cMsgCallbackInfo(cMsgCallback callback, Object arg) {
        this.arg = arg;
        this.callback = callback;
    }
}
